<p><span style="font-family: 'Poiret One'; font-size: 18px; font-weight: bold; color: #10abea;">MINI-URGENCE</span></p>
<p>&nbsp;</p>
<p>En plus des plages horaires laiss&eacute;es libres par votre propre m&eacute;decin pour des rendez-vous accord&eacute;s dans les 24-72 heures pour des urgences mineures, ainsi que de la participation des m&eacute;decins &agrave; la Clinique d'urgences mineures du GMF en soir&eacute;e, les weekends et les f&eacute;ri&eacute;s, la Clinique M&eacute;dicale de Kingsey Falls offre des services de mini-urgence:</p>
<p>&nbsp; &nbsp; - les <span style="text-decoration: underline;">mercredis soirs</span> de <span style="text-decoration: underline;">17h30 &agrave; 20h30</span>*</p>
<p>Pour vous &eacute;viter d'attendre, il ne s'agit pas d'une clinique sans-rendez-vous mais bien de rendez-vous qui sont accord&eacute;s dans un d&eacute;lai rapide suite &agrave; votre appel.&nbsp;<span>Veuillez noter que ces rendez-vous de d&eacute;pannage sont offerts pour les probl&egrave;mes dits urgents (fi&egrave;vre &eacute;lev&eacute;e, otite, pneumonie, infection urinaire, entorse, coupure, ...) et qu'un probl&egrave;me chronique sera dirig&eacute; vers un rendez-vous usuel avec votre propre m&eacute;decin.</span></p>
<p>&nbsp;</p>
<p><span style="font-style: italic;">*Ce service est accord&eacute; de fa&ccedil;on exclusive aux gens ayant un m&eacute;decin de famille &agrave; la clinique</span>.</p>
<p>&nbsp;</p>
<p><span style="font-size: 11px; font-weight: bold;">Si toutefois vous pr&eacute;sentez un probl&egrave;me de sant&eacute; majeur et urgent, veuillez vous pr&eacute;senter directement &agrave; l'urgence de l'H&ocirc;tel-Dieu-d'Arthabaska ou faire le 911.</span></p>
<p>&nbsp;</p>
